// comparison operators
var x = 5;
console.log(x > 4); // true
console.log(x > 5); // false
console.log(x >= 5); // true
console.log(x >= 4); // true
console.log(x < 5); // false 
console.log(x <= 5); // true
console.log(x == 4); // false
console.log(x == 5); // true
console.log(x === 5); // true

x = "5";
console.log(x == 5); // true
console.log(x === 5); // false

console.log(x === "5"); // true

x = 5;
console.log(x);


console.log(x != 4); // true
console.log(x != 5); // false
console.log(x !== 5); // false

x = "5";
console.log(x); // 5

console.log(x !== 5); // true





