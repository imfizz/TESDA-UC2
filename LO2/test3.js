// Booleans
var iLikeMeat = true;
console.log(iLikeMeat); // true

// make it true
iLikeMeat = true;
console.log(iLikeMeat);

// make it false
iLikeMeat = false; 
console.log(iLikeMeat);

console.log(7 > 5); // true 
console.log(7 < 5); // false 
console.log(7 == 7); // true
console.log(7 == 5); // false

// using a constructor 
console.log(Boolean(7 > 5)); // true
console.log(Boolean(6)); // true because it is a number
console.log(Boolean(-5)); // true, even the negative value is a number

console.log(Boolean(0)); // 0 and -0 is the only number that returns false

console.log(Boolean("hello")); // true, the string has a value 
console.log(Boolean("")); // false, it has no value





